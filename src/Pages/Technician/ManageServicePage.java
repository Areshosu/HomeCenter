    public ManageServicePage() {
        initComponents();
        this.setVisible(true);
        DefaultComboBoxModel areas = new DefaultComboBoxModel();
        for (String area: Service.getAreas()) {
            areas.addElement(area);
        }
        areaField.setModel(areas);
        serviceTable.setSelectionModel(new DeselectOnReselectModel());
        serviceTable.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                if (!event.getValueIsAdjusting()) {
                    if (selectedRowIndex == serviceTable.getSelectedRow()) {
                        selectedRowIndex = -1;
                        serviceTable.getSelectionModel().clearSelection();
                    }
                    
                    if (serviceTable.getSelectedRow() == -1) {
                        titleField.setText("");
                        descField.setText("");
                        priceField.setText("");
                        areaField.setSelectedIndex(0);
                        return;
                    }
                    
                    selectedRowIndex = serviceTable.getSelectedRow();
                    titleField.setText(serviceTable.getValueAt(serviceTable.getSelectedRow(), 0).toString());
                    descField.setText(serviceTable.getValueAt(serviceTable.getSelectedRow(), 1).toString());
                    priceField.setText(serviceTable.getValueAt(serviceTable.getSelectedRow(), 2).toString());
                    String selectedArea = serviceTable.getValueAt(serviceTable.getSelectedRow(), 3).toString();
                    areaField.setSelectedIndex(SharedHelper.indexOf(Service.getAreas(), selectedArea));    
                }
            }
        });
    }
    

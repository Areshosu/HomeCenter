    private void createUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUpdateBtnActionPerformed
        String serviceTitle = titleField.getText();
        String serviceDesc = descField.getText();
        Double price = Double.parseDouble(priceField.getText());
        String selectedArea = areaField.getSelectedItem().toString();
        
        if (serviceTitle.length() < 1 ||
                    serviceDesc.length() < 1 ||
                    price < 0 ||
                    selectedArea.length() < 1) {
            formMessage.setText("Invalid details, please try again.");
            formMessage.setForeground(Color.red);
            return;
        }
        
        Service service = new Service(LoginPage.technicianPage.getLoginEmail(), serviceTitle, serviceDesc, price, selectedArea);
        if (selectedRowIndex != -1) {
            Database.updateService(service, selectedRowIndex);
            formMessage.setText("Successfully updated!");
        } else {
            Database.addService(service);
            formMessage.setText("Successfully added!");            
        }
        
        Database.writeToServices();
        refreshTable();
        formMessage.setForeground(Color.green);
    }//GEN-LAST:event_createUpdateBtnActionPerformed

